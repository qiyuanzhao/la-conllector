    private static void start() throws IOException {
        BufferedReader reader = Files.newBufferedReader(Paths.get("/Users/zeng.zhao/Desktop/red_book(芬达).json"));
        JsonMapper mapper = JsonMapper.buildNormalBinder();
        Set<String> contents = new HashSet<>();
        String line = reader.readLine();
        while (line != null) {
            NonceMessage message = mapper.fromJson(line, NonceMessage.class);
            if (message == null) {
                line = reader.readLine();
                continue;
            }
            contents.add(message.getContent());
            line = reader.readLine();
        }

        contents.forEach(content -> {
            try {
                com.google.common.io.Files
                        .asCharSink(Paths.get("/Users/zeng.zhao/Desktop/red_book(芬达).txt").toFile(),
                                StandardCharsets.UTF_8, FileWriteMode.APPEND)
                        .write(content + "\n");
            } catch (IOException e) {
                e.printStackTrace();
            }
        });
    }

